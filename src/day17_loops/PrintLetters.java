package day17_loops;

public class PrintLetters {
    public static void main(String[] args) {

        char first = 'A';
        while (first <= 'Z'){
            System.out.print(first++);
        }
        System.out.println();
        char bckwards = 'z';
        while (bckwards >= 'a'){
            System.out.print(bckwards--);

        }














    }
}
