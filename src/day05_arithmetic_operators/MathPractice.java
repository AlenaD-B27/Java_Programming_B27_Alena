package day05_arithmetic_operators;

public class MathPractice {
    public static void main(String[] args) {

        System.out.println(4+5);
        System.out.println(4*5);
        // int * int --> int
        System.out.println(2*5.5);
        //int * double --> double
        //if you use bigger type in formula the result will be in bigger type
        System.out.println(5%2); // 2+2 -> 2 went into 5 twice, there's one leftover.
        System.out.println(4%2); // no leftover
        System.out.println(4%10); // we can not even do it, so 4 IS a leftover.
//        System.out.println(5%0);    // not gonna work as we can not divide by 0 in math.








    }
}
